function [qweights,qnodes]=Gauss_triangle_nodes_weights_ref(n_gp_2D)
%   computes a n_gp_2D=7 point Gauss quadrature rule of order 6
%   (exact for all polynomials up to degree 5) on the reference element.
%   computes a n_gp_2D=13 point Gauss quadrature rule of order 
%   (exact for all polynomials up to degree 7) on the reference element.
%   computes a n_gp_2D=16 point Gauss quadrature rule of order 
%   (exact for all polynomials up to degree 8) on the reference element.
n_gp_2D=7;

switch n_gp_2D
    case 1
        qnodes=[1/3,1/3];
    case 3
        qnodes=[1/2,0;...
                1/2,1/2;
                0,1/2];
    case 7
        s1=(9+2*sqrt(15))/21;
        s2=(9-2*sqrt(15))/21;
        t1=(6-sqrt(15))/21;
        t2=(6+sqrt(15))/21;
        qnodes=[t1,t1;...
                s1,t1;...
                t1,s1;...
                t2,t2;...
                s2,t2;...
                t2,s2;...
                1/3,1/3];
            
    case 13
        vert=[0,1,0;0,0,1];
        x=zeros(2,13);
        x(:,1) = 1/3*(vert(:,1) + vert(:,2) + vert(:,3));
        x(:,2) = 0.260345966079040*vert(:,1) + ...
            0.260345966079040*vert(:,2) + ...
            0.479308067841920*vert(:,3);
        x(:,3) = 0.260345966079040*vert(:,1) + ...
            0.479308067841920*vert(:,2) + ...
            0.260345966079040*vert(:,3);
        x(:,4) = 0.479308067841920*vert(:,1) + ...
            0.260345966079040*vert(:,2) + ...
            0.260345966079040*vert(:,3);
        x(:,5) = 0.065130102902216*vert(:,1) + ...
            0.065130102902216*vert(:,2) + ...
            0.869739794195568*vert(:,3);
        x(:,6) = 0.065130102902216*vert(:,1) + ...
            0.869739794195568*vert(:,2) + ...
            0.065130102902216*vert(:,3);
        x(:,7) = 0.869739794195568*vert(:,1) + ...
            0.065130102902216*vert(:,2) + ...
            0.065130102902216*vert(:,3);
        x(:,8) = 0.312865496004874*vert(:,1) + ...
            0.638444188569810*vert(:,2) + ...
            0.048690315425316*vert(:,3);
        x(:,9) = 0.638444188569810*vert(:,1) + ...
            0.048690315425316*vert(:,2) + ...
            0.312865496004874*vert(:,3);
        x(:,10)= 0.048690315425316*vert(:,1) + ...
            0.312865496004874*vert(:,2) + ...
            0.638444188569810*vert(:,3);
        x(:,11)= 0.312865496004874*vert(:,1) + ...
            0.048690315425316*vert(:,2) + ...
            0.638444188569810*vert(:,3);
        x(:,12)= 0.638444188569810*vert(:,1) + ...
            0.312865496004874*vert(:,2) + ...
            0.048690315425316*vert(:,3);
        x(:,13)= 0.048690315425316*vert(:,1) + ...
            0.638444188569810*vert(:,2) + ...
            0.312865496004874*vert(:,3);
        qnodes=x';
    case 16
        qnodes=[0.333333333333330   0.333333333333330
               0.459292588292720   0.459292588292720
               0.459292588292720   0.081414823414550
               0.081414823414550   0.459292588292720
               0.170569307751760   0.170569307751760
               0.170569307751760   0.658861384496480
               0.658861384496480   0.170569307751760
               0.050547228317030   0.050547228317030
               0.050547228317030   0.898905543365940
               0.898905543365940   0.050547228317030
               0.263112829634640   0.728492392955400
               0.728492392955400   0.008394777409960
               0.008394777409960   0.263112829634640
               0.728492392955400   0.263112829634640
               0.263112829634640   0.008394777409960
               0.008394777409960   0.728492392955400];
end

switch n_gp_2D
    case 1
        qweights=1/2;
    case 3
        qweights=1/6*ones(1,3)';
    case 7
        c1=(155-sqrt(15))/1200;
        c2=(155+sqrt(15))/1200;
        c3=9/40;
        qweights=1/2*[c1,c1,c1,c2,c2,c2,c3]';
         % 上面三角形区域上的7个高斯点求积公式，对次数小于等于5的多项式准确成立
         % 下面13个高斯点的公式精度要比上面更高,对于次数小于等于7的多项式准确成立
    case 13
        gw=zeros(13,1);
        gw(1) = -0.149570044467682;
        gw(2:4) = 0.175615257433208*ones(1,3);
        gw(5:7) = 0.053347235608838*ones(1,3);
        gw(8:13) = 0.077113760890257*ones(1,6);
        qweights=1/2*gw;
    case 16
        qweights=[0.072157803838895
            0.047545817133640
            0.047545817133640
            0.047545817133640
            0.051608685267360
            0.051608685267360
            0.051608685267360
            0.016229248811600
            0.016229248811600
            0.016229248811600
            0.013615157087215
            0.013615157087215
            0.013615157087215
            0.013615157087215
            0.013615157087215
            0.013615157087215]/2;
end 




return